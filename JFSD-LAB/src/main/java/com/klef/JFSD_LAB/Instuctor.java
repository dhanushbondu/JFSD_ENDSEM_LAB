package com.klef.JFSD_LAB;

public class Instuctor {
		int instructureid;
		String name;
		String email;
		int phinenumber;
		public int getInstructureid() {
			return instructureid;
		}
		public void setInstructureid(int instructureid) {
			this.instructureid = instructureid;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public String getEmail() {
			return email;
		}
		public void setEmail(String email) {
			this.email = email;
		}
		public int getPhinenumber() {
			return phinenumber;
		}
		public void setPhinenumber(int phinenumber) {
			this.phinenumber = phinenumber;
		}
		
}
